function hoverEffect(element) {
  element.style.transform = 'scale(1.1)';
}

function resetEffect(element) {
  element.style.transform = 'scale(1)';
}

function showBio(member) {
  const bioText = document.getElementById('bio-text');
  bioText.textContent = `${member} is a valued team member with expertise in their field.`;
  document.getElementById('bio-modal').style.display = 'block';
}

function closeBio() {
  document.getElementById('bio-modal').style.display = 'none';
}

// Click outside the modal to close it
window.onclick = function(event) {
  const modal = document.getElementById('bio-modal');
  if (event.target === modal) {
      modal.style.display = "none";
  }
}



